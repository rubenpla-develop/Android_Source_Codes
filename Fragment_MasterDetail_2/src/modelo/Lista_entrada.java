package modelo;

public class Lista_entrada {
	public String id;
	public int idImagen;
	public String txtTituloDetalle;
	public String txtDescDetalle;

	public Lista_entrada(String id, int idImagen, String txtTituloDetalle,
			String txtDescDetalle) {
		super();
		this.id = id;
		this.idImagen = idImagen;
		this.txtTituloDetalle = txtTituloDetalle;
		this.txtDescDetalle = txtDescDetalle;
	}

}
